// Start coding here
function add(a, b) {
  let result = a + b;
  return result;
}

function subtract(a, b) {
  let result = a - b;
  return result;
}

function multipy(a, b) {
  let result = a * b;
  return result;
}

function divide(a, b) {
  let result = a / b;
  return result;
}

console.log(add(10, 5));
console.log(subtract(10, 5));
console.log(multipy(10, 5));
console.log(divide(10, 5));
