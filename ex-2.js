// Start coding here
const add = function (a, b) {
  let result = a + b;
  return result;
};

const subtract = function (a, b) {
  let result = a - b;
  return result;
};

const multipy = function (a, b) {
  let result = a * b;
  return result;
};

const divide = function (a, b) {
  let result = a / b;
  return result;
};

console.log(add(10, 5));
console.log(subtract(10, 5));
console.log(multipy(10, 5));
console.log(divide(10, 5));
